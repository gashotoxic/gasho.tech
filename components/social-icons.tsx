const iconMap: Record<string, React.ElementType> = {
  facebook: FacebookIcon,
  x: XIcon,
  linkedin: LinkedinIcon,
  instagram: InstagramIcon,
  threads: ThreadsIcon,
  tiktok: TiktokIcon,
}

interface SocialIconsProps {
  className?: string
  iconClassName?: string
}

export function SocialIcons({ className = "", iconClassName = "" }: SocialIconsProps) {
  return (
    <div className={`flex items-center justify-center gap-3 ${className}`}>
      {socialLinks.map((link: SocialLink) => {
        const Icon = iconMap[link.icon]
        if (!Icon) return null
        return (
          <a
            key={link.name}
            href={link.url}
            target="_blank"
            rel="noopener noreferrer"
            className={`text-[#555] hover:text-[#1abc9c] dark:text-[#e1e1e1] dark:hover:text-[#1abc9c] transition-all duration-300 hover:scale-110 ${iconClassName}`}
            aria-label={link.name}
          >
            <Icon className="h-5 w-5" />
          </a>
        )
      })}
    </div>
  )
}
